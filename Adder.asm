ORG 100h
CODE:
    MOV AL, 19
    MOV BL, 0
    ADD AL, BL

	CMP AL, 1001b
	JA CORRECT_THE_RESULT
	OR AH, 0
	JMP DISPLAY_THE_RESULT

	CORRECT_THE_RESULT: 
		ADD AL, 0110b
		ADD AH, 1
		OR AH, 1
		JMP DISPLAY_THE_RESULT

	DISPLAY_THE_RESULT:
	AND AL, 0FH
    ADD AX, 3030H

    MOV DL, AH
    MOV DH, AL

    MOV AH, 02H
    INT 21H

    MOV DL, DH
    MOV AH, 02H
    INT 21H

	RET
